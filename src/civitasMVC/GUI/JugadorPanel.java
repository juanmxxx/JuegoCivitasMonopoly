    Jugador jugador;
    
    void setJugador(Jugador newjugador){
        jugador = new Jugador(newjugador);
        jTextField1.setText(jugador.getNombre());
        jTextField2.setText(String.valueOf(jugador.getSaldo()));
        if(jugador.esEspeculador()) jTextField3.setText("True");
        else jTextField3.setText("False");
        rellenaPropiedades(jugador.getPropiedades());
        this.repaint();
        this.revalidate();
    }
    
    private void rellenaPropiedades (ArrayList<CasillaCalle> lista){
        //Se elimina la informacion antigua
        propiedadesPanel.removeAll();
        //Se recorre la lista de propiedades para ir creando sus vistas individuales y a√±adirlas al panel
        for (CasillaCalle t : lista) {
            PropiedadPanel vistaPropiedad = new PropiedadPanel();
            vistaPropiedad.setPropiedad(t);
            
            propiedadesPanel.add(vistaPropiedad);
            vistaPropiedad.setVisible(true);
        }
        //Se fuerza la actualizacion visual del panel de propiedades y del panel del jugador
        repaint();
        revalidate();
    }
    
